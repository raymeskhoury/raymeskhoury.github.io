function toAud(dayString, amount) {
  const rate = USD_TO_AUD[dayString];
  if (!rate) {
    throw new CustomFunctions.Error(
      CustomFunctions.ErrorCode.invalidValue,
      "Timestamp not found."
    );
  }

  return amount * rate;
}

function computeConversion(
  dayString,
  dataForMonth,
  reverseCurrencyOrder,
  conversionTimestamp,
  amount
) {
  for (let i = 0; i < dataForMonth.length; i++) {
    const kline = dataForMonth[i];
    // Unpack data
    const openTime = kline[0];
    const openPrice = parseFloat(kline[1]);
    const high = parseFloat(kline[2]);
    const low = parseFloat(kline[3]);

    let conversionPrice = 0;
    if (conversionTimestamp == openTime) {
      // If the timestamp exactly matches the open time, use that price.
      conversionPrice = openPrice;
    } else if (conversionTimestamp < openTime || i + 1 == dataForMonth.length) {
      // Should never happen.
      throw new CustomFunctions.Error(
        CustomFunctions.ErrorCode.invalidValue,
        "Timestamp not found."
      );
    } else if (conversionTimestamp >= dataForMonth[i + 1][0]) {
      // The data isn't in this kline.
      continue;
    } else {
      // The data is in this kline. Use the average of high/low.
      conversionPrice = (high + low) / 2;
    }

    if (reverseCurrencyOrder) {
      conversionPrice = 1 / conversionPrice;
    }

    return toAud(dayString, amount * conversionPrice);
  }

  throw new CustomFunctions.Error(
    CustomFunctions.ErrorCode.invalidValue,
    "Timestamp not found."
  );
}

